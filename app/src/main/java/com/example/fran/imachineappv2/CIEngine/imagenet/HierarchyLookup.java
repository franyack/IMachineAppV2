package com.example.fran.imachineappv2.CIEngine.imagenet;

public class HierarchyLookup{
    private String w1;
    private String w2;

    public HierarchyLookup(String w1, String w2){
        this.w1 = w1;
        this.w2 = w2;
    }

    public String getW1() {
        return w1;
    }

    public String getW2() {
        return w2;
    }
}
